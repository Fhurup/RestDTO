package exceptions;

/**
 *
 * @author fh
 */
public class ExceptionDTO {
    private String message;
    private int code;

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public ExceptionDTO(int code, String message) {
        this.message = message;
        this.code = code;
    }
           
}
